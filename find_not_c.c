#include "libft.h"

int		find_not_c(char c, char *s, int *n)
{
	int i;

	i = 0;
	if (!s)
		return (0);
	while (s[i])
	{
		if (s[i] != c)
		{
			if (n)
				*n = i;
			return (1);
		}
		i++;
	}
	return (0);
}
