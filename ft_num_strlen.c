#include "libft.h"

int		ft_num_strlen(const char *s)
{
	int l;

	if (!s)
		return (0);
	l = s[0] == '-' || s[0] == '+' ? 1 : 0;
	while (ft_isdigit(s[l]))
		l++;
	return (l);
}
