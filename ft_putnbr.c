#include "libft.h"

void	ft_putnbr(int n)
{
	int		dn;
	char	c;

	if (n == -2147483648)
	{
		write(1, "-2147483648\0", 12);
		return ;
	}
	n < 0 ? write(1, "-", 1) : 0;
	n = n < 0 ? -n : n;
	dn = 1;
	while (dn < n / 10)
		dn *= 10;
	while (dn > 0)
	{
		c = n / dn + '0';
		n %= dn;
		dn /= 10;
		write(1, &c, 1);
	}
}
