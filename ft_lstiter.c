#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list *t;

	if (!lst || !f)
		return ;
	t = lst;
	while (t)
	{
		f(t);
		t = t->next;
	}
}
